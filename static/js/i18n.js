function cgTranslate(key) {
  const lang = localStorage.getItem('cg_language') || 'en';
  return CG_TRANSLATIONS[lang]?.[key] || CG_TRANSLATIONS.en[key] || key;
}

function applyLanguage(lang) {
  if (!CG_TRANSLATIONS[lang]) lang = 'en';
  localStorage.setItem('cg_language', lang);

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.body.dir = lang === 'ar' ? 'rtl' : 'ltr';

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = cgTranslate(key);
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = cgTranslate(key);
  });

  const activeModule = document.querySelector('[data-academy-module].active');
  if (activeModule) {
    const mod = activeModule.getAttribute('data-academy-module');
    if (mod === 'password' && typeof renderPasswordTrainer === 'function') renderPasswordTrainer();
    if (mod === 'phishing' && typeof renderPhishingTrainer === 'function') renderPhishingTrainer();
    if (mod === 'quiz' && typeof renderSecurityQuiz === 'function') renderSecurityQuiz();
    if (mod === 'network' && typeof renderNetworkDefense === 'function') renderNetworkDefense();
  }

  if (typeof window.__cgInitQuiz === 'function') window.__cgInitQuiz();
}

